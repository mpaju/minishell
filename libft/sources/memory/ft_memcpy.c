#include "libft.h"

void	*ft_memcpy(void *str1, const void *str2, size_t n)
{
	size_t	c;

	c = 0;
	while (n--)
	{
		((char *)str1)[c] = ((char *)str2)[c];
		c++;
	}
	return (str1);
}
